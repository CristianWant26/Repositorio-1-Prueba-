 ¿Qué es POO?
 
 Es un paradigma de la programación que utiliza objetos y sus interacciones para diseñar applicaciones y programa
 
 ----------------------------
 ¿Qué es un objeto?
 
 Es la representación de una entidad en el mundo real dento de un programa.
 
 
 (coches, muñeca, persona, paisaje(conjunto de objetos(identidad), por ende se puede programar)
 Si lo puedes imaginar, lo puedes programar  *****
 
 EJEMPLO DE IDENTIDAD:
 
 Objetos de la identidad "PORSCHE":
 
 Frenos, volante, llantas, faros, volante
 ------------------------------
 
 
 CARACTERÍSTICAS DE UN OBJETO:
 
 ESTADO: Representado por los atributos del objetio que almacenan información en específico.
 
 COMPORTAMIENTO: Definido por los métodos del objeto, que describen acciones que puede realizar (acelerar, correr, etc)
 
 IDENTIDAD: Cada objeto tiene una identidad unica que lo distingue de otros objetos , incluso si tienen el mismo estado y comportamiento.
 
 ---------------------------------
 
 
 ¿QUË ES UNA CLASE?
 Es una platnilla o molde que define las propiedades(atributos) y comportamientos(métodos) comunes de un tipo de objeto. En otras palabras es el molde para crear un objeto.
            |
            |
            |
           \|/
            V
 OBJETO: Instancia de una clase con valores concretos asignados a sus atributos.
  
  
  
